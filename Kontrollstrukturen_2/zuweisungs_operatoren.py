# Zuweisungen erfolgen in Python mit dem Gleichheitszeichen.
# Wir speichern also den Wert in einem Objekt, indem wir Links des
# Gleichheitszeichens einen Namen vergeben, auf den der Objektinput gerichtet ist.

print("\n#############")
print("\n###### += #######")
a = 1
a += 1
print(a)

print("\n###### *= #######")
a *= 2
print(a)

print("\n###### -= #######")
a -= 1
print(a)

print("\n###### /= #######")
a /= 2
print(a)


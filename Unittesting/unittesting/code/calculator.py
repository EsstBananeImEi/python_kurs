class Calc(object):

    def sum_list(self, args):
        result = sum(args)
        return result

    def addiere(self, a, b):
        result = a + b
        return result

    def subtrahieren(self, a, b):
        result = a - b
        return result

    def multiplizieren(self, a, b):
        result = a * b
        return result

    def dividieren(self, a, b):
        result = a / b

        return result

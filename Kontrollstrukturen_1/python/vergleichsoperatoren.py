# Mit if Anweisungen können wir kontrollen durchführen

"""
    Vergleichsoperatoren:
    Operator | Name                     | Example
        ==   | Equal	                | x == y
        !=	 | Not equal	            | x != y
        >	 | Greater than	            | x > y
        <	 | Less than	            | x < y
        >=	 | Greater than or equal to	| x >= y
        <=	 | Less than or equal to	| x <= y

"""

print("#############")
a = "Paul"
print(a == "Paul")
print(a == "Maria")
print(a != "Maria")

print("\n#############")
print(5 == 5)
print(2 == 5)
print(2 > 5)
print(2 < 5)

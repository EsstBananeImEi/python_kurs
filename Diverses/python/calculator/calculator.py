class Calculator:

    def addiere(zahl_1: float, zahl_2: float) -> float:
        return zahl_1 + zahl_2

    def substrahiere(zahl_1: float, zahl_2: float) -> float:
        return zahl_1 - zahl_2

    def multipliziere(zahl_1: float, zahl_2: float) -> float:
        return zahl_1 * zahl_2

    def dividiere(zahl_1: float, zahl_2: float) -> float:
        return zahl_1 / zahl_2
